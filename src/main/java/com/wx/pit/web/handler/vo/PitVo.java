package com.wx.pit.web.handler.vo;

public class PitVo {
    private String num;
    private String toplateral;
    private String topvertical;
    private String slant;
    private String deeplateral;
    private String columnvertical;
    private String waterlevel;
    private String supportforce;
    private String boltstress;
    private String gap;


    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getToplateral() {
        return toplateral;
    }

    public void setToplateral(String toplateral) {
        this.toplateral = toplateral;
    }

    public String getTopvertical() {
        return topvertical;
    }

    public void setTopvertical(String topvertical) {
        this.topvertical = topvertical;
    }

    public String getSlant() {
        return slant;
    }

    public void setSlant(String slant) {
        this.slant = slant;
    }

    public String getDeeplateral() {
        return deeplateral;
    }

    public void setDeeplateral(String deeplateral) {
        this.deeplateral = deeplateral;
    }

    public String getColumnvertical() {
        return columnvertical;
    }

    public void setColumnvertical(String columnvertical) {
        this.columnvertical = columnvertical;
    }

    public String getWaterlevel() {
        return waterlevel;
    }

    public void setWaterlevel(String waterlevel) {
        this.waterlevel = waterlevel;
    }

    public String getSupportforce() {
        return supportforce;
    }

    public void setSupportforce(String supportforce) {
        this.supportforce = supportforce;
    }

    public String getBoltstress() {
        return boltstress;
    }

    public void setBoltstress(String boltstress) {
        this.boltstress = boltstress;
    }

    public String getGap() {
        return gap;
    }

    public void setGap(String gap) {
        this.gap = gap;
    }
}
